    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String newName = jTextFieldEdit.getText().trim();
        if (newName.length() < 1) return;
        Mediatype mt = mediatypes.get(jListMediatypes.getSelectedIndex());
        mt.setType(newName);
        db.execute(mt, DBAction.update);
        populateList();
        parent.updateMediaList();
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
        Mediatype mt = mediatypes.get(jListMediatypes.getSelectedIndex());
        if (mt == null) return;
        if(JOptionPane.showConfirmDialog(this, "Vill du radera vald mediatyp?","Radera?",JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
            db.execute(mt, DBAction.remove);
            populateList();
        }
    }//GEN-LAST:event_jButtonDeleteActionPerformed

    private void jButtonNewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonNewActionPerformed
        String newName = JOptionPane.showInputDialog(this, "Ny mediatyp");
        if (newName == null) return;
        
        Mediatype mt = new Mediatype();
        mt.setType(newName);
        db.execute(mt, DBAction.add);
        populateList();
    }//GEN-LAST:event_jButtonNewActionPerformed

