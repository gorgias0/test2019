    private void populateList() {
        this.mediatypes = db.getMediatypes();
        mediatypes.sort(null);
        DefaultListModel model = new DefaultListModel();
        mediatypes.forEach(mt -> model.addElement(mt));
        jListMediatypes.setModel(model);
    }

