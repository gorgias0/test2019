    private void jListMediatypesValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jListMediatypesValueChanged
        if (jListMediatypes.getSelectedIndex() < 0) return;
        jTextFieldEdit.setText(mediatypes.get(jListMediatypes.getSelectedIndex()).getType());
    }//GEN-LAST:event_jListMediatypesValueChanged

